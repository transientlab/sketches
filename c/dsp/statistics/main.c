double calc_mean(double *signal_array, int signal_length) {
    double _mean = 0.0;

    for(int i=0; i<signal_length; i++) {
        _mean += signal_array[i];
    }

    _mean = _mean/(double)(signal_length);

    return _mean;
}

double calc_variance(double *signal_array, double signal_mean, int signal_length) {
    double _variance = 0.0;
    
    for(int i=0;i<signal_length;i++) {
        _variance = _variance + pow((signal_array[i]-signal_mean), 2);
    }

    _variance = _variance / (signal_length-1);

    return _variance;
}

double calc_std(double signal_variance) {
    double _std = 0.0;

    _std = pow(signal_variance, 0.5);

    return _std;
}

void convolution(   double *source_signal_array, 
                    double *dest_signal_array, 
                    double *impulse_response, 
                    int buffer_length, 
                    int imprsp_length) {
    int i, j;
    for(i=0; i<buffer_length+imprsp_length; i++) {
        dest_signal_array[i] = 0;
    }
    for(i=0; i<buffer_length; i++) {
        for(j=0; j<imprsp_length; j++) {
            dest_signal_array[i+j] = dest_signal_array[i+j] + source_signal_array[i]*impulse_response[j];
        }
    }
    
}